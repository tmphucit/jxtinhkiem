--date: 2003/3/10


function main(sel)

UTask_tm = GetTask(2);

if (UTask_tm == 12) then						--唐门十级任务过程中
Talk(5, "", "孙婉：真是气死我了！黑心烂肝的龟儿子！", "玩家：这位姑娘，发生了什么事？", "孙婉：刚才在街上，有人拿着一包东西，说是天衣无缝针，那天衣无缝针十分稀罕，我当然想要了，结果就花了十两银子买下来。哪知道回来打开一看，里面包的竟是一把黑漆漆的细针，根本没法绣花！你说气不气人！", "玩家：（正是我要找的魔雨针！幸好偷针的人不识货）孙姑娘，反正这针你也用不上，不如给我吧！", "孙婉：这针可是我花了十两银子买来的，怎能随便给你？如果你真的想要，就拿一只玛瑙戒指来换吧。")
SetTask(2, 14)
return
end;


if (UTask_tm == 14) then						--唐门十级任务过程中
	if ( HaveItem("玛瑙戒指") == 1) then				--有玛瑙戒指
		Say("孙婉：啊！好漂亮的玛瑙戒指！这包针你拿去吧！", 0)
		DelItem("玛瑙戒指")
		AddEventItem("魔雨针")
		Msg2Player("得到魔雨针。")
		SetTask(2, 18)
	else								--没有玛瑙戒指
		Say("孙婉：如果你真的想要那包针，就拿一只玛瑙戒指来换吧。", 0)
	end;
return	
end;


Say("孙婉：绣女的命好苦，年年给他人做嫁衣，可是自己的青春却没有着落……",0);

end;