function yes()

	i = random(0, 5);
	j = 100 * (10 + i);

	Say("游人：你答应卖给我的，不要反悔哦！让我看看我身上还有多少钱，一五一十十五二十……好了给你"..j.."两银子好了。", 0 )
	DelItem("字画")
	Earn(j)
	SetTask(47, 3)
end;

