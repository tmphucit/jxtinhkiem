--两湖区 南岳镇 茶摊老板对话

function main(sel)

i = random(0,2)

if (i == 0) then
Say("茶摊老板：客官，来到衡山脚下，一定要尝尝我们这里的云雾茶。",0)
return
end;

if (i == 1) then
Say("茶摊老板：“名山名水出名茶”，云雾茶是我们衡山的特产，久负盛名，早在唐代就被列为贡品。",0)
return
end;

if (i == 2) then
Say("茶摊老板：云雾茶滋味醇厚，香气浓郁，纯而不淡，浓而不涩。沏上一壶，不仅可以生津解乏，还能够消暑解毒呢。",0)
end;

end;

