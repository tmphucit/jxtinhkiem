--西北南区 凤翔府 酒楼老板对话

function main(sel)

i = random(0,3)

if (i == 0) then
Say("酒楼老板：俺们凤翔可是个好地方，有蓝盈盈的天，有红艳艳的花，有俊俏的姑娘和小伙儿，还有清冽醇香的甘泉佳酿――西凤美酒。",0)
return
end;

if (i == 1) then
Say("酒楼老板：西凤酒产自俺们凤翔，因为凤翔位于关中西部，所以叫做西凤酒。", 0)
return
end;

if (i == 2) then
Say("酒楼老板：这西凤酒已经有好几百年的历史了，从唐代起，就被列为酒中珍品进贡给皇宫内苑。", 0)
return
end;

if (i == 3) then
Say("酒楼老板：全凤翔最好的西凤酒，出自俺柳林酒坊，所以俺这酒坊，在凤翔可是大大的有名。", 0)
end;

end;