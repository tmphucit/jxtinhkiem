--中原南区 襄阳府 茶博士对话

function main(sel)

i = random(0,2)

if (i == 0) then
Say("茶博士：自唐代起，我们湖北就不断出产名茶。如圻门团黄、荆州仙人茶、松滋碧涧茶、归州白茶、黄州松萝茶等，品种繁多，产量丰饶。", 0)
return
end;

if (i == 1) then
Say("茶博士：湖北虽然产茶极盛，但是茶农的日子却苦不堪言，这全是因为朝廷征收的茶税太过繁重所致。", 0)
return
end;

if (i == 2) then
Say("茶博士：茶农们忍受不了朝廷这种侵民渔利的行径，好多地方都已经拔茶种桑，不复产茶了。", 0)
end;

end;


