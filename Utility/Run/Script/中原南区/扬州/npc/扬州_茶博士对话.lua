--中原南区 扬州府 茶博士对话

function main(sel)

i = random(0,3)

if (i == 0) then
Say("茶博士：茶乃天下第一雅品，品茶乃天下第一雅事。人人都知品茶有讲究，其实沏茶也有大学问。",0)
return
end;

if (i == 1) then
Say("茶博士：沏茶有下投、中投、上投之分。冬季下投，先茶后汤；春秋中投，汤半下茶，复以汤满；夏季上投，先汤后茶。",0)
return
end;

if (i == 2) then
Say("茶博士：汤有三辨：一曰形辨，有虾目、蟹目、鱼眼、连珠、涌泉；二曰声辨，有初声、转声、振声、骤声；三曰气辨，有轻雾、淡烟、凝云、布露。",0)
return
end;

if (i == 3) then
Say("茶博士：饮茶应以糕点果脯点茶，好比饮酒要有下酒菜……",0)
end;

end;

